def replace_in_file(filepath):
    try:
        with open(filepath, 'r', encoding='utf-8', errors='ignore') as f:
            content = f.read()
        
        original_content = content
        
        # Sort keys by length descending to ensure phrases are replaced before words
        sorted_keys = sorted(TRANSLATIONS.keys(), key=len, reverse=True)
        
        for cn in sorted_keys:
            if cn in content:
                content = content.replace(cn, TRANSLATIONS[cn])
                
        if content != original_content:
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(content)
            return True
        return False
    except Exception as e:
        print(f"Error processing {filepath}: {e}")
        return False

def main():
    print("Starting Cleanup Chinese -> English translation...")
    dart_files = glob.glob('lib/**/*.dart', recursive=True)
    
    count = 0
    for filepath in dart_files:
        if replace_in_file(filepath):
            print(f"Updated: {filepath}")
            count += 1
            
    print(f"\nFinished! Updated {count} files.")

if __name__ == '__main__':
    main()

